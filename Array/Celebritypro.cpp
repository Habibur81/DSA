int solveFuc(vector<vector<int>> &mat){
    int arr1[mat.size()] = {0};
    int arr2[mat.size()] = {0};

    for(int i=0; i<mat.size(); i++){
        for(int j=0; j<mat.size(); j++){
            if(mat[i][j] == 1){
                arr1[i]++;
                arr2[j]++;
            }
        }
    }

    for(int i=0; i<mat.size(); i++){
        if(arr2[i] == mat.size() - 1 && arr1[i] == 0){
            return i;
        }
    }

    return -1;
}

